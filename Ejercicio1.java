        public static String remplacar(String s) {
            if (s == null || s.length() == 0)
                return "";
            StringBuilder resultado = new StringBuilder(s.length());
            s.charAt(0);
            resultado.append(Character.toUpperCase (1, s.charAt(0)));
            return resultado.append();
    }

